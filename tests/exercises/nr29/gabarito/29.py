#29)  Dado três valores, A, B e C, construa um programa para verificar se estes valores
#podem ser valores dos lados de um triângulo


A = input("Digite o primeiro lado do triangulo: ")
B = input("Digite o segundo lado do triangulo: ")
C = input("Digite o terceiro lado do triangulo: ")

if A + B + C == 180 :
   print ("São valores dos lados de um triângulo")
else :
    print ("Não São valores dos lados de um triângulo")

   
   
